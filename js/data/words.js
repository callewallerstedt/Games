export function isValidWord(word, len) {
  const w = word.toLowerCase().trim();
  return w.length === len && sets[len]?.has(w);
}

export function scoreGuess(secret, guess) {
  const n = secret.length;
  const result = Array(n).fill("absent");
  const remaining = secret.toLowerCase().split("");
  const letters = guess.toLowerCase().split("");
  for (let i = 0; i < n; i++) {
    if (letters[i] === remaining[i]) {
      result[i] = "correct";
      remaining[i] = null;
      letters[i] = null;
    }
  }
  for (let i = 0; i < n; i++) {
    if (!letters[i]) continue;
    const idx = remaining.indexOf(letters[i]);
    if (idx >= 0) {
      result[i] = "present";
      remaining[idx] = null;
    }
  }
  return result;
}
